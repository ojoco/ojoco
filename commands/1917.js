const mumaker = require('mumaker')

module.exports = async (ctx) => {
  const { reply, sock, args, msg } = ctx

  const text = args.join(' ').trim()

  if (!text) {
    return reply(
      '📝 *1917 Text Effect*\n\n' +
      'Usage:\n' +
      '.1917 Your Text Here\n\n' +
      'Example: .1917 Admin Blue'
    )
  }

  await reply('⏳ Generating 1917 style text effect...')

  try {
    const result = await mumaker.ephoto(
      'https://en.ephoto360.com/1917-style-text-effect-523.html', 
      text
    )

    if (!result || !result.image) {
      return reply('❌ Failed to generate image. Please try again.')
    }

    await sock.sendMessage(msg.key.remoteJid, {
      image: { url: result.image },
      caption: `1917 Style Effect\n\nGenerated by VANGUARD MD`,
    }, { quoted: msg })

  } catch (error) {
    console.error('1917 command error:', error)
    await reply('❌ Error generating 1917 effect: ' + error.message)
  }
}